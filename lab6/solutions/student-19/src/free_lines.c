#include "../inc/txtfile.h"

void	free_lines(char **lines) // Функция освобождения памяти массива строк, cначала освобождаем каждую строку по отдельности, потом освобождаем сам массив указателей.
{
	int	i;

	if (!lines) // если массив NULL, то ничего делать не нужно
		return ;
	i = 0;
	while (lines[i]) // проходим по всем строкам массива
	{
		free(lines[i]); // освобождаем каждую строку
		i++;
	}
	free(lines); // освобождаем сам массив указателей
}