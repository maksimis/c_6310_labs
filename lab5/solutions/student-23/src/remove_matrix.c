#include "../inc/matrix.h"   // подключаем описание структуры матрицы
#include <stdlib.h>          // подключаем free для освобождения памяти

void remove_matrix(matrix_t *m) // функция освобождает память матрицы
{
    int i; // счётчик строк

    if (!m) // проверяем, существует ли матрица
        return; // если нет — выходим

    i = 0; // начинаем с первой строки
    while (i < m->rows) // проходим по всем строкам
    {
        free(m->matrix[i]); // освобождаем память каждой строки
        i++; // переходим к следующей строке
    }
    free(m->matrix); // освобождаем массив указателей на строки
    free(m); // освобождаем саму структуру матрицы
}