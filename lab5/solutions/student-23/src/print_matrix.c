#include "../inc/matrix.h"   // подключаем описание структуры матрицы и функций

void print_matrix(matrix_t *m) // функция выводит матрицу в консоль
{
    int i; // счётчик строк
    int j; // счётчик столбцов

    if (!m || !m->matrix) // проверяем, что матрица существует
        return;           // если нет — выходим из функции

    i = 0; // начинаем с первой строки
    while (i < m->rows) // проходим по всем строкам
    {
        j = 0; // начинаем с первого столбца
        while (j < m->columns) // проходим по всем столбцам
        {
            ft_putnbr_fd(m->matrix[i][j], 1); // выводим элемент матрицы

            if (j < m->columns - 1) // если это не последний элемент строки
                ft_putchar_fd(' ', 1); // печатаем пробел между числами

            j++; // переходим к следующему столбцу
        }
        ft_putchar_fd('\n', 1); // после строки делаем перенос строки
        i++; // переходим к следующей строке
    }
}