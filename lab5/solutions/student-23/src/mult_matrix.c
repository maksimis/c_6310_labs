#include "../inc/matrix.h"   // подключаем заголовочный файл с описанием матриц

matrix_t	*mult_matrix(matrix_t *A, matrix_t *B) // функция перемножает две матрицы
{
	matrix_t	*C;   // результат умножения матриц
	int			i;   // счётчик строк первой матрицы
	int			j;   // счётчик столбцов второй матрицы
	int			k;   // счётчик для умножения элементов

	if (!A || !B)   // проверяем, что матрицы существуют
		return (NULL);  // если нет — возвращаем NULL

	if (A->columns != B->rows) // проверяем, можно ли умножать матрицы
		return (NULL);  // если размеры не подходят — ошибка

	C = create_matrix(A->rows, B->columns); // создаём матрицу результата
	if (!C)  // проверяем успешность создания
		return (NULL); // если ошибка — выходим

	i = 0; // начинаем проход по строкам A
	while (i < A->rows) // цикл по строкам
	{
		j = 0; // начинаем проход по столбцам B
		while (j < B->columns) // цикл по столбцам
		{
			C->matrix[i][j] = 0; // обнуляем текущий элемент результата
			k = 0; // начинаем суммирование произведений
			while (k < A->columns) // цикл по элементам строки/столбца
			{
				C->matrix[i][j] += // добавляем произведение элементов
					A->matrix[i][k] * B->matrix[k][j];
				k++; // переходим к следующему элементу
			}
			j++; // следующий столбец результата
		}
		i++; // следующая строка результата
	}
	return (C); // возвращаем итоговую матрицу
}