#include "../inc/matrix.h"   // подключаем заголовочный файл с описанием структуры matrix_t
#include <stdlib.h>          // подключаем библиотеку для malloc и free

matrix_t *create_matrix(int rows, int cols) // функция создаёт матрицу заданного размера
{
    matrix_t *m;   // создаём указатель на структуру матрицы
    int i;         // переменная для цикла по строкам
    int j;         // переменная для цикла по столбцам

    if (rows <= 0 || cols <= 0) // проверяем, что размеры матрицы корректные
        return (NULL);          // если нет — возвращаем NULL

    m = malloc(sizeof(matrix_t)); // выделяем память под саму структуру матрицы
    if (!m)                        // проверяем, удалось ли выделение памяти
        return (NULL);            // если нет — возвращаем NULL

    m->rows = rows;        // сохраняем количество строк
    m->columns = cols;     // сохраняем количество столбцов

    m->matrix = malloc(sizeof(int *) * rows); // выделяем память под массив указателей на строки
    if (!m->matrix)                        // проверяем успешность выделения памяти
    {
        free(m);                          // освобождаем структуру
        return (NULL);                    // выходим с ошибкой
    }

    i = 0; // начинаем цикл по строкам
    while (i < rows) // проходим по каждой строке
    {
        m->matrix[i] = malloc(sizeof(int) * cols); // выделяем память под столбцы текущей строки
        if (!m->matrix[i]) // проверяем успешность выделения памяти
        {
            while (--i >= 0)   // освобождаем уже выделенные строки
                free(m->matrix[i]);
            free(m->matrix);    // освобождаем массив указателей
            free(m);            // освобождаем структуру
            return (NULL);      // выходим с ошибкой
        }

        j = 0; // начинаем цикл по столбцам
        while (j < cols) // проходим по всем столбцам
        {
            m->matrix[i][j] = 0; // инициализируем элемент нулём
            j++;                 // переходим к следующему столбцу
        }
        i++; // переходим к следующей строке
    }
    return (m); // возвращаем готовую матрицу
}